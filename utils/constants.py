def country_name(bbox_key: str, lang: str = 'uk') -> str:
    """Return the country display name for the given language.

    bbox_key is a COUNTRY_BBOXES key (flag emoji + Ukrainian name).
    For lang='en' returns the English name from COUNTRY_NAMES_EN (with fallback
    to the original key).
    """
    if lang == 'en':
        return COUNTRY_NAMES_EN.get(bbox_key, bbox_key)
    return bbox_key

